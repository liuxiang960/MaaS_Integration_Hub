export function getTimeSub(nS) {
  var date = new Date(nS); //获取一个时间对象

  let y = date.getFullYear(); // 获取完整的年份(4位,1970)

  let m =
    date.getMonth() + 1 < 10
      ? "0" + (date.getMonth() + 1)
      : date.getMonth() + 1; // 获取月份(0-11,0代表1月,用的时候记得加上1)

  let d = date.getDate() < 10 ? "0" + date.getDate() : date.getDate(); // 获取日(1-31)

  return y + "-" + m + "-" + d;
}

